# Strings
name = "Booyah"
print(name)
print('This is also a string. ')

# Changing Case in Strings
name = "booyah pablo"
print(name.title()) # Booyah Pablo
print(name.upper()) # BOOYAH PABLO
print(name.lower()) # booyah pablo